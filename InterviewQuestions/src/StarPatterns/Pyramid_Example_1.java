package StarPatterns;

public class Pyramid_Example_1 {

	public static void main(String[] args) {
		
	
		/* Pattern
		 
		   *
		   * *
		   * * *
		   * * * *
		   * * * * *
		   
		  */
		
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print(" *");
			}
			System.out.println();
		}
		
	}
	
	
	}


